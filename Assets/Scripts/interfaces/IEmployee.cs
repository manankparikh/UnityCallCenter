using System;

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>
///   The Employee Designation Enum.  Used To Define The Available Escalation Levels Of A Call.
/// </summary>
/// <remarks> Manan Parikh </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////
public enum EEmployeeDesignation : byte
	{
		Fresher = 0,
		TeamLeader,
		ProjectManager
	}

  ////////////////////////////////////////////////////////////////////////////////////////////////////
  /// <summary>
  ///   The Employee Interface.  Used As The Base For All Employees.  Employees Are Personelle That
  ///   Can Handle Calls That Are Recieved From The Call Center.
  /// </summary>
  /// <remarks> Manan Parikh </remarks>
  ////////////////////////////////////////////////////////////////////////////////////////////////////
	public interface IEmployee
	{

    
		void SetID( uint ID );
		
		uint GetID();
		EEmployeeDesignation GetDesignation();

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Gets The Problem IDs That This Instance Supports Handling. </summary>
		/// <returns> This Instance's Employee Designation. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		EProblemID GetSupportedProblemIDs();

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    ///   Sets This Instance's Busy Status.  An Employee Is Busy When It Is Currently Handling A Call.
    /// </summary>
    /// <param name="bBusy"> Specifies Whether This Instance Is Busy Or Not. </param>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
		void SetBusy( bool bBusy = true );

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Gets Whether This Instance Is Currently Busy Handling A Call. </summary>
		/// <returns> Whether This Instance Is Currently Busy Handling A Call. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		bool IsBusy();
	}